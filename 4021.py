# Чтение входных данных
n, m = map(int, input().split())

# Инициализация таблицы динамического программирования
dp = [[0] * m for _ in range(n)]
dp[0][0] = 1  # Начальная клетка (левая верхняя)

# Заполнение таблицы dp
for i in range(n):
    for j in range(m):
        # Проверяем, можем ли попасть из клетки (i-2, j-1)
        if i - 2 >= 0 and j - 1 >= 0:
            dp[i][j] += dp[i-2][j-1]
        # Проверяем, можем ли попасть из клетки (i-1, j-2)
        if i - 1 >= 0 and j - 2 >= 0:
            dp[i][j] += dp[i-1][j-2]

# Ответ: количество путей в правом нижнем углу
print(dp[n-1][m-1])
